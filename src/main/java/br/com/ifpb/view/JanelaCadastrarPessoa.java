    private void botaoExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoExcluirActionPerformed
        // TODO add your handling code here:

//        System.out.println("Linha selecionada: "+jTProdutos.getSelectedRow());
        if (jTPessoas.getSelectedRow() != -1) {

            Pessoa pessoa = new Pessoa();
            PessoaDao pessoaDao = new PessoaDao();

            pessoa.setCpf((String) jTPessoas.getValueAt(jTPessoas.getSelectedRow(), 0));

            pessoaDao.remover(pessoa);

            txtNome.setText("");
            txtCpf.setText("");
            txtIdade.setText("");
            txtAltura.setText("");
            txtPeso.setText("");

            readJTable();

        } else {
            JOptionPane.showMessageDialog(null, "Selecione uma pessoa para excluir.");
        }


    }//GEN-LAST:event_botaoExcluirActionPerformed

    private void jTPessoasMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTPessoasMouseClicked
        // TODO add your handling code here:

        if (jTPessoas.getSelectedRow() != -1) {

            txtNome.setText(jTPessoas.getValueAt(jTPessoas.getSelectedRow(), 0).toString());
            txtCpf.setText(jTPessoas.getValueAt(jTPessoas.getSelectedRow(), 1).toString());
            txtIdade.setText(jTPessoas.getValueAt(jTPessoas.getSelectedRow(), 2).toString());
            txtAltura.setText(jTPessoas.getValueAt(jTPessoas.getSelectedRow(), 4).toString());
            txtPeso.setText(jTPessoas.getValueAt(jTPessoas.getSelectedRow(), 5).toString());

        }

    }//GEN-LAST:event_jTPessoasMouseClicked

