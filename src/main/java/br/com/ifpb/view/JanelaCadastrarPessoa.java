    public void readJTable() {

        DefaultTableModel modelo = (DefaultTableModel) jTPessoas.getModel();
        modelo.setNumRows(0);
        PessoaDao pessoaDao = new PessoaDao();

        for (Pessoa pessoa : pessoaDao.listar()) {

            modelo.addRow(new Object[]{
                pessoa.getNome(),
                pessoa.getCpf(),
                pessoa.getIdade(),
                pessoa.getAltura(),
                pessoa.getPeso(),
                pessoa.calculaImc()
            });

        }

    }

    public void readJTableForDesc(String desc) {

        DefaultTableModel modelo = (DefaultTableModel) jTPessoas.getModel();
        modelo.setNumRows(0);
        PessoaDao pessoaDao = new PessoaDao();

        for (Pessoa pessoa : pessoaDao.buscaPorDesc(desc)) {

            modelo.addRow(new Object[]{
                pessoa.getNome(),
                pessoa.getCpf(),
                pessoa.getIdade(),
                pessoa.getAltura(),
                pessoa.getPeso(),
                pessoa.calculaImc()
            });

        }

    }

