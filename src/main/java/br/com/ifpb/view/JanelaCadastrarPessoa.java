    private void botaoAtualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoAtualizarActionPerformed
        // TODO add your handling code here:

        if (jTPessoas.getSelectedRow() != -1) {

            Pessoa pessoa = new Pessoa();
            PessoaDao pessoaDao = new PessoaDao();

            pessoa.setNome(txtNome.getText());
            pessoa.setCpf(txtCpf.getText());
            pessoa.setIdade(Integer.parseInt(txtIdade.getText()));
            pessoa.setAltura((float) jTPessoas.getValueAt(jTPessoas.getSelectedRow(), 0));
            pessoa.setPeso((float) jTPessoas.getValueAt(jTPessoas.getSelectedRow(), 0));
            pessoaDao.alterar(pessoa);

            txtNome.setText("");
            txtCpf.setText("");
            txtIdade.setText("");
            txtAltura.setText("");
            txtPeso.setText("");

            readJTable();

        }


    }//GEN-LAST:event_botaoAtualizarActionPerformed

